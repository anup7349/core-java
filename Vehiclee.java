class Vehiclee {
	String model;
	String shade;

	public Vehicle(String model, String shade) {
		this.model = model;
		this.shade = shade;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getModel() {
		return model;
	}

	public void setShade(String shade) {
		this.shade = shade;
	}

	public String getShade() {
		return shade;
	}


}
